void Hamiton(int arc[11][11], int n)
{
	int i, j;
	for (i = 1;i < n;i++)
	{
		x[i] = -1, visited[i] = 0;
	}
	x[0] = 0;
	visited[0] = 1;
	for (i = 1;i >= 1;)
	{
		visited[x[i]] = 0, x[i] = x[i] + 1;
		while (x[i] < n)
		{
			if (visited[x[i]] == 0 && arc[x[i - 1]][x[i]] != 0)
			{
				break;
			}
			else
			{
				x[i] = x[i] + 1;
			}
		}
		if (x[i] == n)
		{
			visited[x[i]] = 0, x[i--] = -1;
		}
		else if (i < n - 1)
		{
			visited[x[i]] = 1, i++;
		}
		else
		{
			if (arc[x[i]][0] != 0)
			{
				for (j = 0;j < n;j++)
				{
					cout << x[j] + 1 << "";
				}
				return;
			}
			else
			{
				visited[x[i]] = 0, x[i--] = -1;
			}
		}
	}
}